    scope :visible_to, ->(user) do
      return all if user.can_view_all_reports?
      return where(user_id: user.id) if user.can_view_assigned_reports?

      none
    end

    scope :ordered, -> do
      order(updated_at: :desc)
    end

    include ViewConfiguration
    include SheetGenerator

    # Report creation

    def run_and_save!
      start
      begin
        populate_universe
        calculate_results
      rescue Exception => e
